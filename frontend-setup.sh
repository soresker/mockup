# Frontend kurulumu
npx create-react-app frontend --template typescript
cd frontend
npm install @mui/material @emotion/react @emotion/styled  # UI framework
npm install @tanstack/react-query  # API state management
npm install axios  # HTTP client
npm install react-router-dom  # Routing
npm install tailwindcss postcss autoprefixer  # Styling
npm install @reduxjs/toolkit react-redux  # State management 